// Smooth Scrolling
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth'
            });
        }
    });
});

// Curriculum Tab Switching
function initCurriculumTabs() {
    const tabs = document.querySelectorAll('.tab-btn');
    const contents = document.querySelectorAll('.phase-content');

    if (tabs.length === 0) return;

    tabs.forEach(tab => {
        tab.addEventListener('click', () => {
            const target = tab.getAttribute('data-phase');

            // Remove active class from all
            tabs.forEach(t => t.classList.remove('active'));
            contents.forEach(c => c.classList.remove('active'));

            // Add active class to target
            tab.classList.add('active');
            document.getElementById(target).classList.add('active');

            // Re-trigger reveal for new content
            reveal();
        });
    });
}

// Trial Form Handling
function initTrialForm() {
    const trialForm = document.getElementById('trialForm');
    const formSuccess = document.getElementById('formSuccess');

    if (!trialForm) return;

    // --- CONFIGURATION ---
    const FORMSPREE_URL = "https://formspree.io/f/mojkjdyg";
    // ---------------------

    trialForm.addEventListener('submit', function (e) {
        e.preventDefault();

        const submitBtn = trialForm.querySelector('button[type="submit"]');
        submitBtn.disabled = true;
        submitBtn.innerText = "...";

        // Get Form Data
        const formData = {
            studentName: document.getElementById('studentName').value,
            parentName: document.getElementById('parentName').value,
            email: document.getElementById('userEmail').value,
            studentAge: document.getElementById('studentAge').value,
            country: document.getElementById('country').value,
            whatsapp: document.getElementById('whatsapp').value,
            prefTime: document.getElementById('prefTime').value
        };

        // Send to Formspree (Automatic Email)
        fetch(FORMSPREE_URL, {
            method: 'POST',
            body: JSON.stringify(formData),
            headers: {
                'Accept': 'application/json',
                'Content-Type': 'application/json'
            }
        }).then(response => {
            // Show Success Overlay
            formSuccess.classList.add('active');
            trialForm.style.display = 'none';
        }).catch(error => {
            console.error('Formspree Error:', error);
            // Even if error, show success to user (or keep form enabled)
            formSuccess.classList.add('active');
            trialForm.style.display = 'none';
        });
    });
}

